package model;

/**
 * Represents the different positions a player can play on the field.
 * Used to categorize players by their role and responsibilities in the team.
 */
public enum Position {
    /** Position for the player who guards the goal and prevents scoring */
    GOALKEEPER,
    /** Position for players who focus on defensive duties */
    DEFENDER,
    /** Position for players who balance offensive and defensive play */
    MIDFIELDER,
    /** Position for players who focus on scoring goals */
    FORWARD
}

